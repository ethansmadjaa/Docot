package Model;

public class Docteur {

    private String nom;

    private String prenom;

    private String lieu;

    private String specialité;

    public Docteur(String nom, String prenom, String lieu, String specialité) {
        this.nom = nom;
        this.prenom = prenom;
        this.lieu = lieu;
        this.specialité = specialité;
    }
}
